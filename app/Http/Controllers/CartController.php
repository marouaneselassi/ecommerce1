     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $prod = Produit::find($id);
        Cart::add($prod->id, $prod->name, 1, $prod->price);
        return redirect("/cart");
    }

